#include <stdio.h>

int main (int argc,char *argv[]){
    
    int edad=45;
    printf ("Mi edad es de %i \n",edad);
    edad+=2;//suma 2
    printf ("Mi edad es de %i \n",edad);
    edad-=2;//resta 2
    printf ("Mi edad es de %i \n",edad);
    edad*=2;//multi 2
    printf ("Mi edad es de %i \n",edad);
    edad/=2;//div 2
    printf ("Mi edad es de %i \n",edad);
   
    return 0;
}